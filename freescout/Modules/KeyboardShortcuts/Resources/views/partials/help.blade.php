<h4 id="shortcuts"><strong>Single Conversation Action</strong></h4>
<p>Listed below shortcuts are available in the single conversation and not available in the list of
    conversations.</p>
<p>Reply&nbsp;– <strong>R</strong><br>
    Add Note&nbsp;– <strong>N</strong><br>
    Send Reply or Note – <strong>CTRL+ENTER / CMD+ENTER</strong><br>
    Forward – <strong>F</strong><br>
    Edit Draft – <strong>E</strong><br>
    New Conversation – <strong>Q</strong><br>
    Delete Conversation – <strong>D</strong><br>
    Next Conversation – <strong>K</strong><br>
    Previous Conversation – <strong>J</strong><br>
    Assign – <strong>A</strong><br>
    Follow – <strong>O</strong><br>
    Status – <strong>S</strong><br>
    Merge – <strong>M</strong><br>
    Move – <strong>V</strong><br>
    Add Tag – <strong>T</strong><br>
    Workflows – <strong>W</strong></p>
<p>When conversation Status dropdown is open (<strong>S</strong> key) the following shortcuts can be
    used to
    change conversation status:</p>
<p>Active&nbsp;– <strong>A</strong><br>
    Pending – <strong>P</strong><br>
    Closed – <strong>C</strong><br>
    Spam – <strong>S</strong><br>
    Not Spam – <strong>N</strong></p>
<h4><strong>Conversations List</strong></h4>
<p>The following shortcuts are available in the list of conversations:</p>
<p>Next Conversation – <strong>K</strong><br>
    Previous Conversation – <strong>J</strong></p>
